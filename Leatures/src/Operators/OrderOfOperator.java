package Operators;

public class OrderOfOperator {
	public static void main(String[] args) {

		System.out.println(8 - 9 * 2);
		System.out.println(9/3/3);
	
		int a = 5;
		int b = 3;
		int c = 2;

		int result = a + b * c;
		System.out.println(result);
		
		int x = 10;
		int y = 3;
		int z = 2;

		int result1 = (x + y) * z;
		System.out.println(result1);
		int m = 8;
		int n = 4;

		m += n * 2;
		System.out.println(m);
	}

}
